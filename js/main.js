function recCiudad(value){
    var parametros = {
        "valor" : value
    };
    $.ajax({
        data: parametros,        
        url: 'https://intranet.canalcapital.gov.co/erpc/mod/radica/views/selmun.php',
        type: 'post',
        success: function(response){
            $("#reloadMun").html(response);
        }
    });
}

